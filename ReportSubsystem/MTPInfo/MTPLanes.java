package ReportSubsystem.MTPInfo;

public class MTPLanes {
	private String numberOfExistingLanes;
	private String numberOfMiles;
	private String numberOfProjectedLanes;
	public String getNumberOfExistingLanes() {
		return numberOfExistingLanes;
	}
	public void setNumberOfExistingLanes(String numberOfExistingLanes) {
		this.numberOfExistingLanes = numberOfExistingLanes;
	}
	public String getNumberOfMiles() {
		return numberOfMiles;
	}
	public void setNumberOfMiles(String numberOfMiles) {
		this.numberOfMiles = numberOfMiles;
	}
	public String getNumberOfProjectedLanes() {
		return numberOfProjectedLanes;
	}
	public void setNumberOfProjectedLanes(String numberOfProjectedLanes) {
		this.numberOfProjectedLanes = numberOfProjectedLanes;
	}
}
