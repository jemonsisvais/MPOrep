package ReportSubsystem.TIPInfo;

public class TIPFederal {
	private String federalFiscalYear;
	private String federalFunctionalC;
	public String getFederalFiscalYear() {
		return federalFiscalYear;
	}
	public void setFederalFiscalYear(String federalFiscalYear) {
		this.federalFiscalYear = federalFiscalYear;
	}
	public String getFederalFunctionalC() {
		return federalFunctionalC;
	}
	public void setFederalFunctionalC(String federalFunctionalC) {
		this.federalFunctionalC = federalFunctionalC;
	}
	
}
