package ReportSubsystem.TIPInfo;

public class TIPLane {
	private String noOfExistingLanes;
	private String noOfMilles;
	private String noOfProjectedLanes;
	public String getNoOfExistingLanes() {
		return noOfExistingLanes;
	}
	public void setNoOfExistingLanes(String noOfExistingLanes) {
		this.noOfExistingLanes = noOfExistingLanes;
	}
	public String getNoOfMilles() {
		return noOfMilles;
	}
	public void setNoOfMilles(String noOfMilles) {
		this.noOfMilles = noOfMilles;
	}
	public String getNoOfProjectedLanes() {
		return noOfProjectedLanes;
	}
	public void setNoOfProjectedLanes(String noOfProjectedLanes) {
		this.noOfProjectedLanes = noOfProjectedLanes;
	}
}
